    server_smoke = None
    if args.server_url:
        print("\n" + "=" * 60)
        print("RUNNING SERVER SMOKE (OPTIONAL)")
        print("=" * 60)
        print(f"Server: {args.server_url}")
        server_smoke = run_server_smoke(args.server_url, output_dir)
