def run_api_drills(server_url, bearer_token, plan_mode, output_dir):
    """Run or plan API-level D1-D6 drills against a live FerrumGate server."""
    token = bearer_token or os.environ.get("FERRUM_BEARER_TOKEN", "")
    headers = {"Content-Type": "application/json"}
    if token:
        headers["Authorization"] = f"Bearer {token}"

    results = {}
    api_output_lines = []
    api_output_lines.append("# D1-D6 API Drill Commands")
    api_output_lines.append(f"*Generated: {datetime.now().isoformat()}*")
    api_output_lines.append(f"*Server: {server_url}*")
    api_output_lines.append(f"*Mode: {'PLAN (dry-run)' if plan_mode else 'LIVE'}*")
    api_output_lines.append("")
    api_output_lines.append("> **LIMITATION**: Full automated API drill execution requires the complete")
    api_output_lines.append("> intent-compile → proposal-evaluate → capability-mint → execution-authorize")
    api_output_lines.append("> lifecycle chain. This script generates the execute-phase payloads only.")
    api_output_lines.append("> Operator must perform the preceding governance steps manually or via ferrumctl.")
    api_output_lines.append("")

    for drill_name, template in API_DRILL_TEMPLATES.items():
        api_output_lines.append(f"## {drill_name}")
        api_output_lines.append("")
        api_output_lines.append(f"**Adapter notes**: {template['adapter_notes']}")
        api_output_lines.append("")

        # Step 1: Intent compile
        api_output_lines.append("### 1. Compile Intent")
        api_output_lines.append(f"```bash")
        api_output_lines.append(
            f"curl -s -X POST '{server_url}/v1/intents/compile' \\")
        if token:
            api_output_lines.append("  -H 'Authorization: Bearer $FERRUM_BEARER_TOKEN' \\")
        api_output_lines.append(f"  -H 'Content-Type: application/json' \\")
        payload_json = json.dumps(template["intent_compile_payload"], indent=2)
        # Indent payload lines for the curl command
        payload_lines = payload_json.split("\n")
        if len(payload_lines) == 1:
            api_output_lines.append(f"  -d '{payload_json}'")
        else:
            api_output_lines.append(f"  -d '{payload_lines[0]}")
            for line in payload_lines[1:-1]:
                api_output_lines.append(f"  {line}")
            api_output_lines.append(f"  {payload_lines[-1]}'")
        api_output_lines.append("```")
        api_output_lines.append("")

        # Step 2: Execute (payload only)
        api_output_lines.append("### 2. Execute Payload")
        api_output_lines.append(
            "Use this payload with POST /v1/executions/{execution_id}/execute after authorize+prepare:"
        )
        api_output_lines.append("```json")
        api_output_lines.append(json.dumps(template["execute_payload"], indent=2))
        api_output_lines.append("```")
        api_output_lines.append("")

        # If not plan mode, attempt a shallow probe
        if not plan_mode:
            try:
                import urllib.request
                req = urllib.request.Request(
                    f"{server_url}/v1/healthz",
                    method="GET",
                    headers=headers if token else {},
                )
                with urllib.request.urlopen(req, timeout=5) as resp:
                    health_ok = resp.status == 200
            except Exception as e:
                health_ok = False
                api_output_lines.append(f"*Shallow probe failed: {e}*")

            results[drill_name] = {
                "success": health_ok,
                "test_count": 1 if health_ok else 0,
                "returncode": 0 if health_ok else 1,
                "output_file": "",
            }
        else:
            results[drill_name] = {
                "planned": True,
                "test_count": 0,
                "returncode": 0,
                "output_file": "",
                "mode": "plan",
            }

    # Write API drill output
    api_output_file = output_dir / "api_drill_commands.md"
    with open(api_output_file, "w", encoding="utf-8") as f:
        f.write("\n".join(api_output_lines))

    return results, str(api_output_file)


