def run_command(cmd, description, timeout=300, capture=True):
    """Run a command and return (success, output, returncode)."""
    print(f"\n{'=' * 60}")
    print(f"Running: {description}")
    print(f"Command: {' '.join(str(c) for c in cmd)}")
    print(f"{'=' * 60}")
    try:
        result = subprocess.run(
            cmd,
            shell=False,
            cwd=str(REPO_ROOT),
            capture_output=capture,
            text=True,
            timeout=timeout,
        )
        success = result.returncode == 0
        output = result.stdout + result.stderr if capture else ""
        return success, output, result.returncode
    except subprocess.TimeoutExpired:
        return False, f"Command timed out after {timeout}s", -1
    except Exception as e:
        return False, str(e), -1


