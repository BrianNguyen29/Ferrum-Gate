    results = {}

    # Run cargo tests if not skipped
    if not args.skip_cargo:
        print("\n" + "=" * 60)
        print("RUNNING D1–D6 CARGO TESTS")
        print("=" * 60)

        for drill_name, cargo_test in DRILL_CARGO_TEST_MAPPING.items():
            result = run_cargo_tests(
                drill_name,
                cargo_test["test_args"],
                output_dir,
            )
