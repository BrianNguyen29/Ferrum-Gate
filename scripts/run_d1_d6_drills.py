def main():
    args = parse_args()

    print("=" * 60)
    print("FerrumGate v1 — D1–D6 Local Drill Runner")
    print("=" * 60)
    print()
    print("IMPORTANT: This script does NOT complete G2/operator signoff.")
    print("           Operator review is still required.")
    print()

    output_dir = ensure_output_dir(args.output_dir)
    print(f"Output directory: {output_dir}")
    print(f"Skip cargo tests: {args.skip_cargo}")
    print(f"Server URL: {args.server_url or 'Not provided (no live server required)'}")
    print(f"API drills: {args.api_drills}")
    print(f"API live: {args.api_live}")
    print(f"Plan mode: {args.plan}")
    print()

    selected_drills = None
    if args.drills:
        selected_drills = set(d.strip().upper() for d in args.drills.split(","))
        print(f"Selected drills: {selected_drills}")

    if args.probe_readyz_deep:
        if not args.server_url:
            print("ERROR: --probe-readyz-deep requires --server-url")
            return 1
        print("=" * 60)
        print("RUNNING READYZ/DEEP DIAGNOSTIC PROBE")
        print("=" * 60)
        if args.plan:
            probe_output_file = output_dir / "readyz_deep_probe_plan.md"
            lines = ["# /v1/readyz/deep Diagnostic Probe Plan\n\n"]
            lines.append(f"*Generated: {_now_rfc3339()}*\n")
            lines.append(f"*Server: {args.server_url}*\n")
            lines.append(f"*Probes: {args.probe_count} at {args.probe_interval}s intervals*\n\n")
            lines.append("Run the following command(s) on the target host:\n\n")
            url = f"{args.server_url}/v1/readyz/deep"
            for i in range(args.probe_count):
                lines.append(f"### Probe {i+1}\n")
                lines.extend(
                    _curl_block("GET", url, {"Content-Type": "application/json"}, None, True)
                )
                lines.append("")
                if i < args.probe_count - 1:
                    lines.append(f"sleep {args.probe_interval}\n")
            with open(probe_output_file, "w", encoding="utf-8") as f:
                f.write("\n".join(lines))
            print(f"\n[PLAN MODE] Probe plan written to: {probe_output_file}")
            return 0
        json_file, md_file, _ = probe_readyz_deep(
            args.server_url,
            args.bearer_token,
            output_dir,
            probe_count=args.probe_count,
            probe_interval=args.probe_interval,
        )
        print(f"\nProbe JSON: {json_file}")
        print(f"Probe Markdown: {md_file}")
        return 0

    if args.dry_run and not (args.api_drills or args.api_live):
        print("[DRY RUN] Would execute the following drills:")
        for drill_name in DRILL_CARGO_TEST_MAPPING:
            key = drill_name.split()[0]
            if selected_drills is None or key in selected_drills:
                cargo_test = DRILL_CARGO_TEST_MAPPING[drill_name]
                print(f"  - {drill_name}: cargo test {' '.join(cargo_test['test_args'])}")
        print(f"  - Server smoke: {args.server_url or 'skipped'}")
        print(f"  - Output dir: {output_dir}")
        return 0

    results = {}

    # API mode (legacy --api-drills or new --api-live)
    api_mode = args.api_drills or args.api_live
    if api_mode:
        if not args.server_url:
            print("ERROR: --api-drills/--api-live requires --server-url")
            return 1
        print("=" * 60)
        print("RUNNING API-LEVEL DRILLS")
        print("=" * 60)
        api_results, api_file = run_api_drills(
            args.server_url,
            args.bearer_token,
            args.plan,
            output_dir,
            args.rate_limit_delay,
            selected_drills=selected_drills,
        )
        results.update(api_results)
        print(f"\nAPI drill output: {api_file}")
        if args.plan:
            print("[PLAN MODE] No live requests were sent.")
        # Skip cargo tests in API mode unless explicitly skipped
        args.skip_cargo = True

    # Run cargo tests if not skipped
    if not args.skip_cargo:
        print("\n" + "=" * 60)
        print("RUNNING D1–D6 CARGO TESTS")
        print("=" * 60)

        for drill_name, cargo_test in DRILL_CARGO_TEST_MAPPING.items():
            key = drill_name.split()[0]
            if selected_drills is not None and key not in selected_drills:
                continue
            result = run_cargo_tests(
                drill_name,
                cargo_test["test_args"],
                output_dir,
            )
            results[drill_name] = result
            status = "PASS" if result["success"] else "FAIL"
            print(f"\n>>> {drill_name}: {status} ({result['test_count']} tests)")

            # Brief pause between tests to avoid resource contention
            time.sleep(0.5)

    # Run optional server smoke
    server_smoke = None
    token_for_smoke = args.bearer_token or os.environ.get("FERRUM_BEARER_TOKEN", "")
    if args.server_url and not args.plan:
        print("\n" + "=" * 60)
        print("RUNNING SERVER SMOKE (OPTIONAL)")
        print("=" * 60)
        print(f"Server: {args.server_url}")
        server_smoke = run_server_smoke(args.server_url, token_for_smoke, output_dir)
        status = "PASS" if server_smoke.get("success") else "FAIL"
        print(f"\n>>> Server smoke: {status}")
    elif args.plan:
        print("\n[SKIPPED] Server smoke (plan mode; no live probes sent)")
    else:
        print("\n[SKIPPED] Server smoke (no --server-url provided)")

    # Generate output files
    print("\n" + "=" * 60)
    print("GENERATING OUTPUT FILES")
    print("=" * 60)

    output_files = []

    # Determine output format
    want_json = args.json_out or (not args.json_out and not args.markdown_out)
    want_md = args.markdown_out or (not args.json_out and not args.markdown_out)

    if want_json:
        json_file = generate_json_summary(
            results, output_dir, server_smoke, plan_mode=args.plan, evidence_label=args.evidence_label
        )
        output_files.append(json_file)
        print(f"Generated JSON: {json_file}")

    if want_md:
        md_file = generate_markdown_summary(
            results, output_dir, server_smoke, plan_mode=args.plan, evidence_label=args.evidence_label
        )
        output_files.append(md_file)
        print(f"Generated markdown: {md_file}")

    # Generate evidence skeletons from raw test output
    if not args.skip_cargo:
        combined_output = []
        for drill_name, result in results.items():
            output_file = result.get("output_file", "")
            if output_file and Path(output_file).exists():
                with open(output_file, "r", encoding="utf-8") as f:
                    combined_output.append(f"# {drill_name}\n{f.read()}")

        if combined_output:
            skeleton_md = generate_skeleton(
                "\n---\n".join(combined_output),
                output_dir,
                "d1-d6",
            )
            output_files.append(skeleton_md)
            print(f"Generated evidence skeleton: {skeleton_md}")

    # Summary
    print("\n" + "=" * 60)
    print("DRILL RUN SUMMARY")
    print("=" * 60)

    total = len(results)
    if args.plan:
        print(f"\nAPI drill plan items: {total}")
        print("No live requests were sent.")
    elif args.api_live:
        passed = sum(1 for r in results.values() if r.get("overall_status") == "passed")
        partial = sum(1 for r in results.values() if r.get("overall_status") == "partial")
        failed = sum(1 for r in results.values() if r.get("overall_status") not in ("passed", "partial"))
        print(f"\nTotal drills: {total}")
        print(f"Passed: {passed}")
        print(f"Partial: {partial}")
        print(f"Failed: {failed}")
    else:
        passed = sum(1 for r in results.values() if r.get("success"))
        failed = total - passed
        print(f"\nTotal drills: {total}")
        print(f"Passed: {passed}")
        print(f"Failed: {failed}")

    if server_smoke:
        smoke_status = "PASS" if server_smoke.get("success") else "FAIL"
        print(f"Server smoke: {smoke_status}")

    print(f"\nEvidence output: {output_dir}")
    print("\nOutput files:")
    for f in output_files:
        print(f"  - {f}")

    print("\n" + "=" * 60)
    print("IMPORTANT NOTES")
    print("=" * 60)
    print("- Evidence is labeled 'local/test-drill' — operator review REQUIRED")
    print("- No G2 gate is completed by this output")
    print("- No production pilot signoff is implied or granted")
    print("- Operator must review evidence per docs/implementation-path/58-*.md")
    print("=" * 60)

    if args.plan:
        return 0

    if args.api_live:
        failed_count = sum(
            1 for r in results.values()
            if r.get("overall_status") not in ("passed", "partial")
        )
        return 0 if failed_count == 0 else 1

    failed_count = sum(1 for r in results.values() if not r.get("success"))
    return 0 if failed_count == 0 else 1


if __name__ == "__main__":
    sys.exit(main())
