def _now_rfc3339():
    return datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")


def _curl_block(method, url, headers, payload, placeholder_token):
    """Generate a markdown curl code block using heredoc for shell-safe payloads."""
    lines = ["```bash"]
    cmd_parts = [f"curl -s -X {method} '{url}'"]
    if placeholder_token:
        cmd_parts.append("  -H 'Authorization: Bearer $FERRUM_BEARER_TOKEN'")
    for k, v in headers.items():
        if k.lower() == "authorization":
            continue
        cmd_parts.append(f"  -H '{k}: {v}'")
    if payload is not None:
        payload_json = json.dumps(payload, indent=2)
        cmd_parts.append("  -d @- <<'PAYLOAD'")
        curl_cmd = " \\\n".join(cmd_parts)
        lines.append(curl_cmd)
        lines.append(payload_json)
        lines.append("PAYLOAD")
    else:
        curl_cmd = " \\\n".join(cmd_parts)
        lines.append(curl_cmd)
    lines.append("```")
    return lines


def run_api_drills(server_url, bearer_token, plan_mode, output_dir, rate_limit_delay, selected_drills=None):
    """Run or plan API-level D1-D6 drills against a live FerrumGate server."""
    token = bearer_token or os.environ.get("FERRUM_BEARER_TOKEN", "")
    headers = {"Content-Type": "application/json"}
    if token:
        headers["Authorization"] = f"Bearer {token}"

    # Determine which drills to run
    drills_to_run = {}
    for drill_name, template in API_DRILL_TEMPLATES.items():
        key = drill_name.split()[0]  # e.g., "D1"
        if selected_drills is None or key in selected_drills:
            drills_to_run[drill_name] = template

    results = {}
    api_output_lines = []
    api_output_lines.append("# D1-D6 API Drill Commands")
    api_output_lines.append(f"*Generated: {_now_rfc3339()}*")
    api_output_lines.append(f"*Server: {server_url}*")
    api_output_lines.append(f"*Mode: {'PLAN (dry-run)' if plan_mode else 'LIVE'}*")
    api_output_lines.append("")
    api_output_lines.append("> **LIMITATION**: Full automated API drill execution requires the complete")
    api_output_lines.append("> intent-compile → proposal-evaluate → capability-mint → execution-authorize")
    api_output_lines.append("> lifecycle chain. Some steps may fail if the target server build does not")
    api_output_lines.append("> wire the requested adapter (e.g., HTTP adapter defaults to noop).")
    api_output_lines.append("")

    for drill_name, template in drills_to_run.items():
        api_output_lines.append(f"## {drill_name}")
        api_output_lines.append("")
        api_output_lines.append(f"**Adapter notes**: {template['adapter_notes']}")
        api_output_lines.append("")

        drill_result = {
            "drill": drill_name,
            "steps": {},
            "overall_status": "planned" if plan_mode else "pending",
        }

        # Step 1: Compile Intent
        step_name = "1_compile_intent"
        api_output_lines.append("### 1. Compile Intent")
        compile_url = f"{server_url}/v1/intents/compile"
        compile_payload = _normalize_intent_compile_payload(template["intent_compile_payload"])
        api_output_lines.extend(_curl_block("POST", compile_url, {"Content-Type": "application/json"}, compile_payload, token))
        api_output_lines.append("")

        intent_id = None
        if not plan_mode:
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("POST", compile_url, headers, compile_payload)
            if status in (200, 201) and isinstance(body, dict) and "envelope" in body:
                intent_id = body["envelope"].get("intent_id")
                if intent_id:
                    drill_result["steps"][step_name] = {"status": "passed", "intent_id": intent_id}
                else:
                    drill_result["steps"][step_name] = {
                        "status": "failed",
                        "http_status": status,
                        "error": "Missing intent_id in compile response envelope",
                        "body": body,
                    }
                    drill_result["overall_status"] = "failed"
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
                drill_result["overall_status"] = "failed"
        else:
            drill_result["steps"][step_name] = {"status": "planned"}

        # Step 2: Evaluate ActionProposal
        step_name = "2_evaluate_proposal"
        api_output_lines.append("### 2. Evaluate ActionProposal")
        proposal_id = str(uuid.uuid4())
        proposal_payload = dict(template["proposal"])
        proposal_payload["proposal_id"] = proposal_id
        proposal_payload["intent_id"] = intent_id or "<INTENT_ID_FROM_STEP_1>"
        proposal_payload["created_at"] = _now_rfc3339()
        evaluate_url = f"{server_url}/v1/proposals/{proposal_id}/evaluate"
        api_output_lines.extend(_curl_block("POST", evaluate_url, {"Content-Type": "application/json"}, proposal_payload, token))
        api_output_lines.append("")

        decision = None
        if not plan_mode and intent_id and drill_result["overall_status"] != "failed":
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("POST", evaluate_url, headers, proposal_payload)
            if status == 200 and isinstance(body, dict):
                decision = body.get("decision")
                drill_result["steps"][step_name] = {"status": "passed", "decision": decision}
                if decision not in ("Allow",):
                    drill_result["overall_status"] = "failed"
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
                drill_result["overall_status"] = "failed"
        elif plan_mode:
            drill_result["steps"][step_name] = {"status": "planned"}
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "previous step failed"}

        # Step 3: Mint Capability
        step_name = "3_mint_capability"
        api_output_lines.append("### 3. Mint Capability")
        capability_payload = dict(template["capability"])
        capability_payload["intent_id"] = intent_id or "<INTENT_ID_FROM_STEP_1>"
        capability_payload["proposal_id"] = proposal_id
        mint_url = f"{server_url}/v1/capabilities/mint"
        api_output_lines.extend(_curl_block("POST", mint_url, {"Content-Type": "application/json"}, capability_payload, token))
        api_output_lines.append("")

        capability_id = None
        if not plan_mode and intent_id and drill_result["overall_status"] != "failed":
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("POST", mint_url, headers, capability_payload)
            if status in (200, 201) and isinstance(body, dict) and "lease" in body:
                capability_id = body["lease"].get("capability_id")
                if capability_id:
                    drill_result["steps"][step_name] = {"status": "passed", "capability_id": capability_id}
                else:
                    drill_result["steps"][step_name] = {
                        "status": "failed",
                        "http_status": status,
                        "error": "Missing capability_id in mint response lease",
                        "body": body,
                    }
                    drill_result["overall_status"] = "failed"
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
                drill_result["overall_status"] = "failed"
        elif plan_mode:
            drill_result["steps"][step_name] = {"status": "planned"}
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "previous step failed"}

        # Step 4: Authorize Execution
        step_name = "4_authorize_execution"
        api_output_lines.append("### 4. Authorize Execution")
        authorize_payload = {
            "proposal_id": proposal_id,
            "capability_id": capability_id or "<CAPABILITY_ID_FROM_STEP_3>",
            "dry_run": False,
        }
        authorize_url = f"{server_url}/v1/executions/authorize"
        api_output_lines.extend(_curl_block("POST", authorize_url, {"Content-Type": "application/json"}, authorize_payload, token))
        api_output_lines.append("")

        execution_id = None
        if not plan_mode and capability_id and drill_result["overall_status"] != "failed":
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("POST", authorize_url, headers, authorize_payload)
            if status == 200 and isinstance(body, dict) and "execution" in body:
                execution_id = body["execution"].get("execution_id")
                drill_result["steps"][step_name] = {"status": "passed", "execution_id": execution_id}
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
                drill_result["overall_status"] = "failed"
        elif plan_mode:
            drill_result["steps"][step_name] = {"status": "planned"}
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "previous step failed"}

        # Step 5: Prepare Execution
        step_name = "5_prepare_execution"
        api_output_lines.append("### 5. Prepare Execution")
        prepare_url = f"{server_url}/v1/executions/{execution_id or '<EXECUTION_ID_FROM_STEP_4>'}/prepare"
        api_output_lines.extend(_curl_block("POST", prepare_url, {"Content-Type": "application/json"}, None, token))
        api_output_lines.append("")

        prepared = False
        if not plan_mode and execution_id and drill_result["overall_status"] != "failed":
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("POST", prepare_url, headers)
            if status == 200 and isinstance(body, dict):
                prepared = body.get("prepared", False)
                drill_result["steps"][step_name] = {"status": "passed" if prepared else "failed", "prepared": prepared}
                if not prepared:
                    drill_result["overall_status"] = "failed"
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
                drill_result["overall_status"] = "failed"
        elif plan_mode:
            drill_result["steps"][step_name] = {"status": "planned"}
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "previous step failed"}

        # Step 6: Execute Adapter Operation
        step_name = "6_execute"
        api_output_lines.append("### 6. Execute Adapter Operation")
        execute_url = f"{server_url}/v1/executions/{execution_id or '<EXECUTION_ID_FROM_STEP_4>'}/execute"
        execute_payload = {"payload": template["execute_payload"]}
        api_output_lines.extend(_curl_block("POST", execute_url, {"Content-Type": "application/json"}, execute_payload, token))
        api_output_lines.append("")

        executed = False
        if not plan_mode and execution_id and drill_result["overall_status"] != "failed":
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("POST", execute_url, headers, execute_payload)
            if status == 200 and isinstance(body, dict):
                executed = body.get("executed", False)
                drill_result["steps"][step_name] = {
                    "status": "passed" if executed else "failed",
                    "executed": executed,
                    "result_digest": body.get("result_digest"),
                }
                if not executed:
                    drill_result["overall_status"] = "failed"
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
                drill_result["overall_status"] = "failed"
        elif plan_mode:
            drill_result["steps"][step_name] = {"status": "planned"}
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "previous step failed"}

        # Step 7: Compensate Execution
        step_name = "7_compensate"
        api_output_lines.append("### 7. Compensate Execution")
        compensate_url = f"{server_url}/v1/executions/{execution_id or '<EXECUTION_ID_FROM_STEP_4>'}/compensate"
        api_output_lines.extend(_curl_block("POST", compensate_url, {"Content-Type": "application/json"}, None, token))
        api_output_lines.append("")

        compensated = False
        if not plan_mode and execution_id and drill_result["overall_status"] != "failed":
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("POST", compensate_url, headers)
            if status == 200 and isinstance(body, dict):
                compensated = body.get("compensated", False)
                drill_result["steps"][step_name] = {"status": "passed" if compensated else "failed", "compensated": compensated}
                # Compensation failure does not necessarily fail the overall drill
                # because some adapters may not support compensation for all operations.
                if not compensated:
                    drill_result["overall_status"] = "partial"
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
                drill_result["overall_status"] = "partial"
        elif plan_mode:
            drill_result["steps"][step_name] = {"status": "planned"}
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "previous step failed"}

        # Step 8: Verify Execution Outcome
        # For compensation drills, verify is not applicable after compensate.
        # The gateway contract requires compensate while the contract is in ExecutedAwaitingVerify;
        # calling verify first would transition to Verified and block compensation.
        step_name = "8_verify"
        api_output_lines.append("### 8. Verify Execution Outcome")
        verify_url = f"{server_url}/v1/executions/{execution_id or '<EXECUTION_ID_FROM_STEP_4>'}/verify"
        api_output_lines.extend(_curl_block("POST", verify_url, {"Content-Type": "application/json"}, None, token))
        api_output_lines.append("")

        if plan_mode:
            drill_result["steps"][step_name] = {
                "status": "planned",
                "note": "Skipped for compensation drills — verify endpoint is for commit-path flows. Compensation drills call compensate immediately after execute while contract is in ExecutedAwaitingVerify.",
            }
        elif execution_id and drill_result["overall_status"] != "failed":
            # Skip live verify for compensation drills to avoid state conflict.
            drill_result["steps"][step_name] = {
                "status": "skipped",
                "reason": "Not applicable for compensation drill — verify transitions contract away from ExecutedAwaitingVerify and would block compensation. Compensation drills rely on post-compensate state checks.",
            }
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "previous step failed"}

        # Step 9: Capture Evidence (query execution record)
        step_name = "9_capture_evidence"
        api_output_lines.append("### 9. Capture Evidence")
        get_url = f"{server_url}/v1/executions/{execution_id or '<EXECUTION_ID_FROM_STEP_4>'}"
        api_output_lines.extend(_curl_block("GET", get_url, {"Content-Type": "application/json"}, None, token))
        api_output_lines.append("")

        if not plan_mode and execution_id:
            time.sleep(rate_limit_delay)
            status, body, err = _make_api_request("GET", get_url, headers)
            if status == 200 and isinstance(body, dict):
                drill_result["steps"][step_name] = {"status": "passed", "execution_record": body}
            else:
                drill_result["steps"][step_name] = {
                    "status": "failed",
                    "http_status": status,
                    "error": err,
                    "body": body,
                }
        elif plan_mode:
            drill_result["steps"][step_name] = {"status": "planned"}
        else:
            drill_result["steps"][step_name] = {"status": "skipped", "reason": "no execution_id"}

        if plan_mode:
            drill_result["overall_status"] = "planned"
        elif drill_result["overall_status"] == "pending":
            drill_result["overall_status"] = "passed"

        results[drill_name] = drill_result

    # Write API drill output
    api_output_file = output_dir / "api_drill_commands.md"
    with open(api_output_file, "w", encoding="utf-8") as f:
        f.write("\n".join(api_output_lines))

    return results, str(api_output_file)


