def run_command(cmd, description, timeout=300, capture=True):
    """Run a command and return (success, output, returncode)."""
    print(f"\n{'=' * 60}")
    print(f"Running: {description}")
    print(f"Command: {' '.join(str(c) for c in cmd)}")
    print(f"{'=' * 60}")
    try:
        result = subprocess.run(
            cmd,
            shell=False,
            cwd=str(REPO_ROOT),
            capture_output=capture,
            text=True,
            timeout=timeout,
        )
        success = result.returncode == 0
        output = result.stdout + result.stderr if capture else ""
        return success, output, result.returncode
    except subprocess.TimeoutExpired:
        return False, f"Command timed out after {timeout}s", -1
    except Exception as e:
        return False, str(e), -1


def ensure_output_dir(output_dir):
    """Create output directory if it doesn't exist."""
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    return output_dir


def run_cargo_tests(drill_name, cargo_args, output_dir):
    """Run cargo tests for a specific drill and capture output."""
    test_output_file = output_dir / f"{drill_name.replace(' ', '_').replace('(', '').replace(')', '')}_test_output.txt"
    combined_output = []

    cmd = ["cargo", "test", "--quiet"] + cargo_args

    success, output, rc = run_command(cmd, f"{drill_name} cargo tests", timeout=300)

    # Save raw output
    with open(test_output_file, "w", encoding="utf-8") as f:
        f.write(f"# {drill_name} Test Output\n")
        f.write(f"# Generated: {datetime.now().isoformat()}\n")
        f.write(f"# Command: {' '.join(str(c) for c in cmd)}\n")
        f.write(f"# Return code: {rc}\n")
        f.write(f"# Success: {success}\n")
        f.write("\n## STDOUT/STDERR:\n\n")
        f.write(output)

    combined_output.append(output)

    test_count = parse_test_count(output)

    # Drill fails if cargo succeeded but found zero tests (pattern matched nothing)
    if success and test_count == 0:
        success = False

    return {
        "drill": drill_name,
        "success": success,
        "returncode": rc,
        "output_file": str(test_output_file),
        "test_count": test_count,
    }


def parse_test_count(output):
    """Parse test count from cargo test output."""
    import re
    match = re.search(r"(\d+) passed", output)
    if match:
        return int(match.group(1))
    match = re.search(r"(\d+) failed", output)
    if match:
        return -int(match.group(1))
    return 0


def run_server_smoke(server_url, output_dir):
    """Run optional server smoke checks if server URL is provided."""
    smoke_output_file = output_dir / "server_smoke_output.txt"
    combined = []

    # Import and run check_pilot_readiness
    sys.path.insert(0, str(REPO_ROOT / "scripts"))
    try:
        from check_pilot_readiness import (
            check_shallow_readiness,
            check_deep_readiness,
            check_functional_readiness,
            check_metrics_endpoint,
        )

        bearer_token = os.environ.get("FERRUMCTL_BEARER_TOKEN", "")

        results = {}
        for name, func in [
            ("shallow_readiness", check_shallow_readiness),
            ("deep_readiness", check_deep_readiness),
            ("functional_readiness", check_functional_readiness),
            ("metrics_endpoint", check_metrics_endpoint),
        ]:
            try:
                success, output = func(server_url, bearer_token)
                results[name] = {"success": success, "output": output[:500]}
            except Exception as e:
                results[name] = {"success": False, "output": str(e)}

        smoke_output = {
            "server_url": server_url,
            "timestamp": datetime.now().isoformat(),
            "results": results,
        }

        with open(smoke_output_file, "w", encoding="utf-8") as f:
            f.write(f"# Server Smoke Output\n")
            f.write(f"# Generated: {datetime.now().isoformat()}\n")
            f.write(f"# Server: {server_url}\n")
            f.write(json.dumps(smoke_output, indent=2))

        return {"success": True, "output_file": str(smoke_output_file), "results": results}

    except ImportError as e:
        return {"success": False, "output_file": str(smoke_output_file), "error": str(e)}


def generate_skeleton(content, output_dir, drill_type="d1-d6"):
    """Generate evidence skeleton using the existing generator script."""
    skeleton_output = output_dir / f"{drill_type}_evidence_skeleton.md"

    # Write content to temp file for skeleton generator
    temp_input = output_dir / f"{drill_type}_raw_input.txt"
    with open(temp_input, "w", encoding="utf-8") as f:
        f.write(content)

    cmd = [
        sys.executable,
        str(SKELETON_GENERATOR),
        "--type", drill_type,
        "--file", str(temp_input),
    ]

    success, output, rc = run_command(cmd, f"Generate {drill_type} evidence skeleton", timeout=30)

    if success:
        with open(skeleton_output, "w", encoding="utf-8") as f:
            f.write(output)
    else:
        # Fallback: write raw content with disclaimer
        with open(skeleton_output, "w", encoding="utf-8") as f:
            f.write(DISCLAIMER)
            f.write(f"\n\n# {drill_type.upper()} Evidence (Raw Output)\n\n")
            f.write(f"*Generated: {datetime.now().isoformat()}*\n\n")
            f.write("```\n")
            f.write(content[:10000])  # Truncate if too long
            f.write("\n```\n")

    return str(skeleton_output)


