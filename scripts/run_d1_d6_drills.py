def generate_json_summary(results, output_dir, server_smoke=None, plan_mode=False):
    """Generate JSON summary of drill results."""
    json_file = output_dir / "drill_summary.json"

    summary = {
        "generated": datetime.now().isoformat(),
        "repository": str(REPO_ROOT),
        "output_dir": str(output_dir),
        "disclaimer": "LOCAL/TEST-DRILL EVIDENCE — OPERATOR REVIEW REQUIRED",
        "run_context": {
            "environment": "local/test-drill only",
            "plan_mode": plan_mode,
            "server_smoke": server_smoke.get("server_url") if server_smoke else None,
            "server_smoke_passed": server_smoke.get("success") if server_smoke else None,
        },
        "drill_results": {},
        "evidence_files": [],
    }

    for drill_name, result in results.items():
        if plan_mode:
            summary["drill_results"][drill_name] = {
                "planned": True,
                "test_count": 0,
                "returncode": 0,
                "output_file": result.get("output_file", ""),
            }
        else:
            summary["drill_results"][drill_name] = {
                "success": result.get("success", False),
                "test_count": result.get("test_count", 0),
                "returncode": result.get("returncode", -1),
                "output_file": result.get("output_file", ""),
            }
        if result.get("output_file"):
            summary["evidence_files"].append(result["output_file"])

    if server_smoke and server_smoke.get("output_file"):
        summary["evidence_files"].append(server_smoke["output_file"])

    with open(json_file, "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    return str(json_file)


