def run_cargo_tests(drill_name, cargo_args, output_dir):
    """Run cargo tests for a specific drill and capture output."""
    test_output_file = output_dir / f"{drill_name.replace(' ', '_').replace('(', '').replace(')', '')}_test_output.txt"
    combined_output = []

    cmd = ["cargo", "test", "--quiet"] + cargo_args

    success, output, rc = run_command(cmd, f"{drill_name} cargo tests", timeout=300)

    # Save raw output
    with open(test_output_file, "w", encoding="utf-8") as f:
        f.write(f"# {drill_name} Test Output\n")
        f.write(f"# Generated: {datetime.now(timezone.utc).isoformat()}\n")
        f.write(f"# Command: {' '.join(str(c) for c in cmd)}\n")
        f.write(f"# Return code: {rc}\n")
        f.write(f"# Success: {success}\n")
        f.write("\n## STDOUT/STDERR:\n\n")
        f.write(output)

    combined_output.append(output)

    test_count = parse_test_count(output)

    # Drill fails if cargo succeeded but found zero tests (pattern matched nothing)
    if success and test_count == 0:
        success = False

    return {
        "drill": drill_name,
        "success": success,
        "returncode": rc,
        "output_file": str(test_output_file),
        "test_count": test_count,
    }


def parse_test_count(output):
    """Parse test count from cargo test output."""
    import re
    match = re.search(r"(\d+) passed", output)
    if match:
        return int(match.group(1))
    match = re.search(r"(\d+) failed", output)
    if match:
        return -int(match.group(1))
    return 0


