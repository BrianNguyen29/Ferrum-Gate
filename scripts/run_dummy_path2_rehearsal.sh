# =============================================================================
# PHASE 2 — LOCAL PROBES
# =============================================================================

phase2_local_probes() {
    echo ""
    echo "########################################"
    echo "# PHASE 2: LOCAL PROBES"
    echo "########################################"

    local phase_dir="$OUTPUT_DIR/01-probes"
    mkdir -p "$phase_dir"

    # Find a free port
    find_free_port() {
        local port=18080
        local max_attempts=100
        local attempt=0
        while ((attempt < max_attempts)); do
            if ! (echo > /dev/tcp/127.0.0.1/$port) 2>/dev/null; then
                echo $port
                return 0
            fi
            port=$((port + 1))
            attempt=$((attempt + 1))
        done
        echo ""
        return 1
    }

    FREE_PORT=$(find_free_port)
    if [[ -z "$FREE_PORT" ]]; then
        echo "[FAIL] Could not find free port"
        log_phase phase2 "failed"
        return 1
    fi

    BASE_URL="http://127.0.0.1:$FREE_PORT"
    DUMMY_TOKEN="dummy-local-test-probe-$(openssl rand -hex 8 2>/dev/null || python3 -c 'import secrets; print(secrets.token_hex(8))')"

    echo "[INFO] Using port: $FREE_PORT"
    echo "[INFO] Base URL: $BASE_URL"

    # Find ferrumd binary
    FERRUMD=""
    for candidate in \
        "$REPO_ROOT/target/release/ferrumd" \
        "$REPO_ROOT/target/debug/ferrumd"; do
        [[ -x "$candidate" ]] && FERRUMD="$candidate" && break
    done

    if [[ -z "$FERRUMD" ]]; then
        echo "[INFO] ferrumd not found; building release..."
        cargo build --release --bin ferrumd --manifest-path "$REPO_ROOT/Cargo.toml" >/dev/null 2>&1
        FERRUMD="$REPO_ROOT/target/release/ferrumd"
    fi

    if [[ ! -x "$FERRUMD" ]]; then
        echo "[FAIL] ferrumd not available"
        log_phase phase2 "failed"
        return 1
    fi

    echo "[INFO] Using ferrumd: $FERRUMD"

    # Create temp config
    local probe_config="$phase_dir/probe-config.toml"
    cat > "$probe_config" << EOF
[server]
bind_addr = "127.0.0.1:$FREE_PORT"
store_dsn = "sqlite::memory:"
auth_mode = "bearer"
bearer_token = "$DUMMY_TOKEN"
allow_insecure_nonlocal_bind = false
log_filter = "info"
EOF

    # Start ferrumd
    local ferrumd_log="$phase_dir/ferrumd.log"
    FERRUMD_BEARER_TOKEN="$DUMMY_TOKEN" "$FERRUMD" --config "$probe_config" > "$ferrumd_log" 2>&1 &
    FERRUMD_PID=$!

    # Wait for server
    wait_for_server() {
        local max_wait=30
        local waited=0
        while ((waited < max_wait)); do
            if python3 -c "
import urllib.request
import urllib.error
try:
    urllib.request.urlopen('http://127.0.0.1:$FREE_PORT/v1/healthz', timeout=1)
    print('ok')
except Exception:
    pass
" 2>/dev/null | grep -q ok; then
                return 0
            fi
            sleep 1
            waited=$((waited + 1))
        done
        return 1
    }

    echo "[INFO] Waiting for ferrumd to be ready..."
    if ! wait_for_server; then
        echo "[FAIL] ferrumd did not become ready"
        echo "--- ferrumd log ---"
        tail -20 "$ferrumd_log" 2>/dev/null || cat "$ferrumd_log" 2>/dev/null || true
        echo "--- end log ---"
        kill "$FERRUMD_PID" 2>/dev/null || true
        log_phase phase2 "failed"
        return 1
    fi

    echo "[INFO] ferrumd is ready"

    # Helper function for probes
    probe() {
        local name="$1"
        local path="$2"
        local out_file="$phase_dir/${name}.txt"

        local http_code
        http_code=$(python3 -c "
import urllib.request
import urllib.error
try:
    req = urllib.request.Request('http://127.0.0.1:$FREE_PORT$path')
    urllib.request.urlopen(req, timeout=5)
    print('200')
except urllib.error.HTTPError as e:
    print(e.code)
except Exception as e:
    print('000')
" 2>/dev/null)

        echo "$http_code" > "$out_file"
        echo "  [PROBE] $path → $http_code"
    }

    # Run probes
    echo ""
    echo "[INFO] Running probe sequence..."

    probe healthz /v1/healthz
    probe readyz /v1/readyz
    probe readyz_deep /v1/readyz/deep

    # Metrics probe (get more than just code)
    python3 -c "
import urllib.request
try:
    req = urllib.request.Request('http://127.0.0.1:$FREE_PORT/v1/metrics')
    resp = urllib.request.urlopen(req, timeout=5)
    print(resp.read().decode()[:500])
except Exception as e:
    print(f'Error: {e}')
" > "$phase_dir/metrics.txt" 2>/dev/null
    echo "  [PROBE] /v1/metrics → captured"

    # Stop ferrumd
    kill "$FERRUMD_PID" 2>/dev/null || true
    wait "$FERRUMD_PID" 2>/dev/null || true
    echo "[INFO] ferrumd stopped"

    echo ""
    echo "[RESULT] Phase 2 (Local Probes): PASSED"
    log_phase phase2 "passed"
    return 0
}

