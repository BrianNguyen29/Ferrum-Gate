phase0_preflight() {
    echo ""
    echo "########################################"
    echo "# PHASE 0: PREFLIGHT"
    echo "########################################"

    FAILED_CHECKS=0

    echo ""
    echo "[INFO] Checking script syntax..."

    # Check bash syntax of this script
    if bash -n "$SCRIPT_DIR/run_dummy_path2_rehearsal.sh" 2>&1; then
        echo "[PASS] run_dummy_path2_rehearsal.sh syntax valid"
    else
        echo "[FAIL] run_dummy_path2_rehearsal.sh has syntax errors"
        FAILED_CHECKS=$((FAILED_CHECKS + 1))
    fi

    # Check referenced scripts exist and have valid syntax
    REFERENCED_SCRIPTS=(
        "$SCRIPT_DIR/run_local_auth_smoke.sh"
        "$SCRIPT_DIR/run_local_restore_drill.sh"
        "$SCRIPT_DIR/run_pre_target_gate.sh"
    )

    for script in "${REFERENCED_SCRIPTS[@]}"; do
        if [[ -f "$script" ]]; then
            if bash -n "$script" 2>&1; then
                echo "[PASS] $(basename "$script") syntax valid"
            else
                echo "[FAIL] $(basename "$script") has syntax errors"
                FAILED_CHECKS=$((FAILED_CHECKS + 1))
            fi
        else
            echo "[SKIP] $(basename "$script") not found"
        fi
    done

    # Check Python scripts exist and have valid syntax
    PYTHON_SCRIPTS=(
        "$SCRIPT_DIR/run_d1_d6_drills.py"
        "$SCRIPT_DIR/generate_evidence_skeleton.py"
    )

    for script in "${PYTHON_SCRIPTS[@]}"; do
        if [[ -f "$script" ]]; then
            if python3 -m py_compile "$script" 2>&1; then
                echo "[PASS] $(basename "$script") syntax valid"
            else
                echo "[FAIL] $(basename "$script") has syntax errors"
                FAILED_CHECKS=$((FAILED_CHECKS + 1))
            fi
        else
            echo "[SKIP] $(basename "$script") not found"
        fi
    done

    # Check canonical docs are NOT touched
    echo ""
    echo "[INFO] Checking canonical docs NOT modified..."
    check_canonical_docs_untouched

    # Check doc 69 exists (source of dummy values)
    if [[ -f "$DUMMY_VALUES_DOC" ]]; then
        echo "[PASS] Dummy values doc exists: 69-local-dummy-target-values.md"
    else
        echo "[WARN] Dummy values doc not found: 69-local-dummy-target-values.md"
    fi

    # Check bundle template exists
    if [[ -f "$BUNDLE_DIR/README.md" ]]; then
        echo "[PASS] Dummy bundle template exists"
    else
        echo "[WARN] Dummy bundle template not found"
    fi

    echo ""
    if [[ $FAILED_CHECKS -eq 0 ]]; then
        echo "[RESULT] Phase 0 (Preflight): PASSED"
        log_phase phase0 "passed"
        return 0
    else
        echo "[RESULT] Phase 0 (Preflight): FAILED ($FAILED_CHECKS checks failed)"
        log_phase phase0 "failed"
        return 1
    fi
}

