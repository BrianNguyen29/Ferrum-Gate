run_check() {
    local name="$1"
    local cmd="$2"
    echo ""
    echo "========================================"
    echo "CHECK: $name"
    echo "========================================"
    if eval "$cmd"; then
        echo "[PASS] $name"
        return 0
    else
        echo "[FAIL] $name"
        return 1
    fi
}

